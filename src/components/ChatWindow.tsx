/**
 * ChatWindow.tsx
 * 
 * 전체 채팅창 컴포넌트
 * 
 * 이 파일의 역할:
 * - 채팅 UI의 메인 컨테이너 컴포넌트
 * - MessageBubble과 InputForm 컴포넌트를 조합하여 완전한 채팅 UI 구성
 * - 채팅 메시지 목록을 표시하고 관리
 * - 스크롤 관리 (새 메시지가 추가되면 자동으로 하단으로 스크롤)
 * - 전체 채팅 레이아웃 및 스타일링 관리
 * 
 * 왜 필요한가:
 * - 채팅 UI의 핵심 컨테이너 역할
 * - 여러 하위 컴포넌트들을 하나로 통합
 * - 채팅 메시지 리스트와 입력 영역을 논리적으로 그룹화
 * - 재사용 가능한 채팅 UI 컴포넌트 제공
 * 
 * 주요 기능:
 * - useChat 훅을 사용하여 채팅 로직 연결
 * - 메시지 목록 렌더링 (MessageBubble 컴포넌트 사용)
 * - 메시지 입력 및 전송 (InputForm 컴포넌트 사용)
 * - 자동 스크롤 처리 (새 메시지가 추가될 때)
 * - 로딩 상태 표시
 * - 에러 상태 처리 및 표시
 * 
 * 컴포넌트 구조:
 * - ChatWindow (메인 컨테이너)
 *   - 메시지 목록 영역
 *     - MessageBubble (각 메시지)
 *   - 입력 영역
 *     - InputForm (입력 필드 및 전송 버튼)
 * 
 * 레이아웃:
 * - 상단: 헤더 (선택적)
 * - 중간: 메시지 목록 (스크롤 가능)
 * - 하단: 입력 폼 (고정)
 * 
 * 상태 관리:
 * - useChat 훅을 통해 메시지 상태 관리
 * - 로컬 상태는 최소화하고 훅에 위임
 * 
 * 사용 예시:
 * - <ChatWindow /> 형태로 간단하게 사용
 * - App.tsx에서 렌더링하여 채팅 화면 표시
 */
