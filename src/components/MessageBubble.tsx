/**
 * MessageBubble.tsx
 * 
 * 메시지 버블 컴포넌트 (유저/봇 메시지 표시)
 * 
 * 이 파일의 역할:
 * - 개별 채팅 메시지를 시각적으로 표시하는 컴포넌트
 * - 사용자 메시지와 봇 메시지를 구분하여 다른 스타일 적용
 * - 메시지 내용, 타임스탬프, 메타데이터 등을 표시
 * - 메시지 타입에 따른 레이아웃 차별화 (왼쪽/오른쪽 정렬)
 * 
 * 왜 필요한가:
 * - 재사용 가능한 메시지 UI 컴포넌트 제공
 * - 메시지 표시 로직을 독립적으로 관리
 * - 사용자와 봇의 메시지를 시각적으로 명확히 구분
 * - 일관된 메시지 UI 스타일 유지
 * - 각 메시지의 독립적인 스타일링 가능
 * 
 * 주요 기능:
 * - MessageBubble 컴포넌트: 메시지 표시
 * - role prop에 따른 스타일 구분 ('user' | 'assistant')
 * - 메시지 내용 표시 (텍스트)
 * - 타임스탬프 표시 (선택적)
 * - 소스 정보 표시 (RAG 응답 시 참조 문서, 선택적)
 * - 로딩 상태 표시 (봇이 응답 중일 때)
 * 
 * 스타일링:
 * - 사용자 메시지: 오른쪽 정렬, 다른 배경색 (예: 파란색)
 * - 봇 메시지: 왼쪽 정렬, 다른 배경색 (예: 회색)
 * - 말풍선 스타일 (둥근 모서리, 그림자 효과 등)
 * - 반응형 디자인 고려
 * 
 * Props:
 * - message: Message 타입 (role, content)
 * - timestamp?: Date (선택적)
 * - sources?: Source[] (RAG 참조 소스, 선택적)
 * - isStreaming?: boolean (스트리밍 중 여부)
 * 
 * 사용 예시:
 * - <MessageBubble message={message} />
 * - ChatWindow에서 messages 배열을 map하여 렌더링
 */
